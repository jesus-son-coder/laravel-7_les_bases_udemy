    {
        $validatedData = $request->validate([
            'title' => 'required|max:150',
            'content' => 'required'
        ]);
        $article = new Article();
        $article->title = $request->input('title');
        $article->content = $request->input('content');
        $article->save();
        return redirect()->back();
    }

